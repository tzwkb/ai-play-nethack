    "            return None\n"
    "        if self._map_cache[cr0][px] not in FLOOR:\n"
    "            return None\n"
    "        room = set()\n"
    "        stack = [(px, cr0)]\n"
    "        while stack:\n"
    "            cx, cr = stack.pop()\n"
    "            if (cx, cr) in room or not (0 <= cr < MAP_ROWS and 0 <= cx < MAP_COLS):\n"
    "                continue\n"
    "            if self._map_cache[cr][cx] not in FLOOR:\n"
    "                continue\n"
    "            room.add((cx, cr))\n"
