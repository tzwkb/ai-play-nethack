    "        ruler_ones = '   ' + ''.join(str(c % 10) for c in range(MAP_COLS))\n"
    "        return ruler_tens + '\\n' + ruler_ones + '\\n' + '\\n'.join(rows)\n"
    "\n"
    "    # BFS pathfinding\n"
    "\n"
    "    def _bfs(self, sx, sy, targets):\n"
    "        WALKABLE = set('.#<>+$%`_' + chr(92) + '{') | MONSTER_SYMS | ITEM_SYMS\n"
    "        visited = {(sx, sy - 1)}\n"
    "        q = deque([(sx, sy - 1, [])])\n"
    "        while q:\n"
    "            cx, cr, path = q.popleft()\n"
    "            if (cx, cr + 1) in targets:\n"
    "                return path, (cx, cr + 1)\n"
    "            for name, dx, dy in DIRS8:\n"
    "                nx, nr = cx + dx, cr + dy\n"
    "                if (nx, nr) in visited:\n"
    "                    continue\n"
    "                if not (0 <= nr < MAP_ROWS and 0 <= nx < MAP_COLS):\n"
    "                    continue\n"
    "                ch = self._map_cache[nr][nx]\n"
    "                if ch not in WALKABLE:\n"
    "                    continue\n"
    "                visited.add((nx, nr))\n"
    "                q.append((nx, nr, path + [name]))\n"
    "        return None, None\n"
