    "                stack.append((cx + dx, cr + dy))\n"
    "        corridors, doors, gaps = set(), set(), set()\n"
    "        for cx, cr in room:\n"
    "            for dx, dy in DIRS4:\n"
    "                nx, nr = cx + dx, cr + dy\n"
    "                if not (0 <= nr < MAP_ROWS and 0 <= nx < MAP_COLS):\n"
    "                    continue\n"
    "                ch = self._map_cache[nr][nx]\n"
    "                coord = (nx, nr + 1)\n"
    "                if ch == '#':\n"
    "                    corridors.add(coord)\n"
    "                elif ch == '+':\n"
    "                    doors.add(coord)\n"
    "                elif ch in '|-':\n"
    "                    ex, er = nx + dx, nr + dy\n"
    "                    if 0 <= er < MAP_ROWS and 0 <= ex < MAP_COLS and self._map_cache[er][ex] == ' ':\n"
