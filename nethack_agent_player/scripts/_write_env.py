    "\n"
    "    # Map cache\n"
    "\n"
    "    def _update_map_cache(self, obs):\n"
    "        chars = obs['tty_chars']\n"
    "        b = obs['blstats']\n"
    "        px, py = int(b[0]), int(b[1])\n"
    "        for r in range(MAP_ROWS):\n"
    "            for c in range(MAP_COLS):\n"
    "                ch = chr(chars[r + 1][c])\n"
    "                if ch in _TERRAIN:\n"
    "                    self._map_cache[r][c] = ch\n"
    "        cr = py - 1\n"
    "        if 0 <= cr < MAP_ROWS and 0 <= px < MAP_COLS:\n"
    "            if self._map_cache[cr][px] == ' ':\n"
    "                self._map_cache[cr][px] = '.'\n"
    "\n"
    "    def _render_map_cache(self, obs):\n"
    "        chars = obs['tty_chars']\n"
    "        rows = []\n"
    "        for r in range(MAP_ROWS):\n"
    "            row = list(self._map_cache[r])\n"
    "            for c in range(MAP_COLS):\n"
    "                ch = chr(chars[r + 1][c])\n"
